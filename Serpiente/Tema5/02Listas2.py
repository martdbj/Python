# Formas de crear una lista
lista1 = []
print("Lista 1 = ", lista1)
lista2 = ["a", 1, True]
print("Lista 2 = ", lista2)
lista3 = list()
print("Lista 3 = ", lista3)
lista4 = list("Hola")
print("Lista 4 = ", lista4)
# Asignar / Admite elementos mutables y repetidos (TEST)
lista1 = [20, 40, 10, 40, 50]
print("Lista 1 = ", lista1)
print("Lista1[:] = ", lista1[:])
print("sorted(lista1) = ", sorted(lista1))
print("sorted(lista1) con reverse = ", sorted(lista1, reverse=True))
